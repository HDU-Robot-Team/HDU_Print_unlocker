void OLED_WriteByte(Uchar mcmd)
{
Uchar length = 8;			// Send Command

	while(length--)
	{
		if(mcmd & 0x80)
		{
			setSDA;
		}
		else
		{
			resetSDA;
		}
//		delay_us(50);
		setSCL;
//		delay_us(50);
		resetSCL;
//		delay_us(50);
		mcmd = mcmd << 1;
	}
}


