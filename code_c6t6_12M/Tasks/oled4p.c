void OLED_SetPos(Uchar x,Uchar page){
	OLED_WriteCommand(0xb0+page);
	OLED_WriteCommand(((x&0xf0)>>4)|0x10);
	OLED_WriteCommand((x&0x0f)|0x01); 
}
void OLED_ShowChar(Uchar x,Uchar y,char chr){//x(0 to 15) y(0 to 3)
	unsigned char c=chr-' ',i=0;
	unsigned char page=y*2;
	x=x*8;				
	if(x>127){
		x=0;
		page=page+2;
	}
	OLED_SetPos(x,page);	
	for(i=0;i<8;i++)OLED_WriteData(F8X16[c*16+i]);
	OLED_SetPos(x,page+1);
	for(i=0;i<8;i++)OLED_WriteData(F8X16[c*16+i+8]);	
}
void OLED_ShowString(Uchar x,Uchar y,char *s){
	int p=0;
	while(*(s+p)!='\0'){
		OLED_ShowChar(x+p,y,*(s+p));
		p++;
	}
}
void OLED_ShowNum(Uchar x,Uchar y,int n,Uchar len){
	int p=0;
	if(n<0){
		OLED_ShowChar(x,y,'-');
		n=-n;
	}else{
		OLED_ShowChar(x,y,' ');
	}
	if(n==0){
		OLED_ShowChar(x+len-1,y,'0');
		p++;
	}
	while(n>0&&p<len){
		OLED_ShowChar(x+len-p-1,y,n%10+48);
		p++;
		n=n/10;
	}
	while(p<len-1){
		OLED_ShowChar(x+len-p-1,y,' ');
		p++;
	}
	
}
