void OLED_WriteCommand(Uchar Data)
{
	I2C_Start3();
	OLED_WriteByte(0x00);
	I2C_Ack3();
	OLED_WriteByte(Data);
	I2C_Ack3();
	I2C_Stop3();
}


void OLED_WriteData(Uchar Data)
{
	I2C_Start3();
	OLED_WriteByte(0x40);
	I2C_Ack3();
	OLED_WriteByte(Data);
	I2C_Ack3();
	I2C_Stop3();
}
